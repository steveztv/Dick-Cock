                          child: Image.file(
                          _image,
                          width: 110,
                          height: 110,
                          fit: BoxFit.cover,
                        ))
                      : SizedBox(
                          width: 110,
                          height: 110,
                          child: Icon(
                            Icons.person,
                            size: 100,
                            color: Colors.grey[800],
                          ),
                        ),
                ),
                Positioned(
                  bottom: -10,
                  left: 70,
                  child: IconButton(
                    onPressed: () async {
