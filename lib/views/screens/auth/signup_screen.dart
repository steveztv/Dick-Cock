              width: MediaQuery.of(context).size.width - 40,
              height: 50,
              decoration: BoxDecoration(
                border: Border.all(color: Colors.black),
                color: buttonColor,
                borderRadius: const BorderRadius.all(
                  Radius.circular(5),
                ),
              ),
              child: InkWell(
                onTap: () => authController.registerUser(
